export type IntegrationTier = "native" | "agent" | "external"

export interface IntegrationEntry {
  /** Unique provider key */
  id: string
  /** Display name */
  name: string
  /** One-line description */
  description: string
  /** Category for filtering */
  category: IntegrationCategory
  /** native = first-party Vercel, agent = AI agent service, external = third-party */
  tier: IntegrationTier
  /** Environment variable names this integration needs */
  envKeys: string[]
  /** Whether this integration uses OAuth flow */
  oauth: boolean
  /** URL to the provider's docs/dashboard */
  docsUrl: string
  /** Whether this integration is featured */
  featured: boolean
  /** Whether this is enabled/available (some are coming soon) */
  available: boolean
  /** Tools this integration provides to agents */
  agentTools: string[]
}

