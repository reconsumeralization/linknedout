// ─── Helpers ──────────────────────────────────────────────────────────────────

/** Get all integrations for a given category */
export function getIntegrationsByCategory(category: IntegrationCategory): IntegrationEntry[] {
  return INTEGRATION_CATALOG.filter((i) => i.category === category)
}

/** Get all integrations for a given tier */
export function getIntegrationsByTier(tier: IntegrationTier): IntegrationEntry[] {
  return INTEGRATION_CATALOG.filter((i) => i.tier === tier)
}

/** Get featured integrations */
export function getFeaturedIntegrations(): IntegrationEntry[] {
  return INTEGRATION_CATALOG.filter((i) => i.featured)
}

/** Get available integrations (excludes coming soon) */
export function getAvailableIntegrations(): IntegrationEntry[] {
  return INTEGRATION_CATALOG.filter((i) => i.available)
}

/** Check if an integration has its env vars configured */
export function isIntegrationConfigured(id: string, env: Record<string, string | undefined>): boolean {
  const entry = INTEGRATION_CATALOG.find((i) => i.id === id)
  if (!entry || entry.envKeys.length === 0) return false
  return entry.envKeys.every((key) => !!env[key])
}

/** Get all unique categories in the catalog */
export function getAllCategories(): IntegrationCategory[] {
  return [...new Set(INTEGRATION_CATALOG.map((i) => i.category))]
}

